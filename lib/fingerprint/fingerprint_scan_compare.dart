class FingerprintRecognitionScanCompare extends StatefulWidget {
  const FingerprintRecognitionScanCompare({Key? key,required this.checkInOrOut}) : super(key: key);
  final checkInOrOut;
  @override
  State<FingerprintRecognitionScanCompare> createState() => _FingerprintRecognitionScanCompareState();
}

class _FingerprintRecognitionScanCompareState extends State<FingerprintRecognitionScanCompare> {

  int authCheck=0;
  static final DeviceInfoPlugin deviceInfoPlugin = DeviceInfoPlugin();
  Map<String, dynamic> _deviceData = <String, dynamic>{};

  var finger;

  @override
  void initState() {
    super.initState();
    _authenticate();
    initPlatformState();
    auth.isDeviceSupported().then(
          (bool isSupported) => setState(() => _supportState = isSupported
          ? _SupportState.supported
          : _SupportState.unsupported
          ),
    );


  }

  @override
  Widget build(BuildContext context) {

    var size=MediaQuery.of(context).size;
    var width = size.width;

    if(  _supportState == _SupportState.supported && authCheck ==0){
      _authenticate();
      authCheck=1;
    }
    else if(  _supportState == _SupportState.supported && authCheck ==1){

      if (_authorized == "Authorized"  ) {
        setState(() {
          authCheck++;
        });
      }

      else if (_authorized == "Not Authorized") {
        authCheck++;
      }


    }




    print("iiiiiiii");
    print(_deviceData['id']);
    print("**************");



    return Scaffold(
      backgroundColor: Colors.white,
      body: Padding(
        padding: const EdgeInsets.all(15.0),
        child: SingleChildScrollView(
          child: Column(
            mainAxisAlignment: MainAxisAlignment.center,
            children: [
              SizedBox(
                height: width/4,
              ),
              Row(
                mainAxisAlignment: MainAxisAlignment.center,
                children: [
                  Text("جاري الفحص",
                    style: TextStyle(
                      fontSize: 25,
                    ),
                  ),
                ],
              ),
              SizedBox(
                height: 200,
              ),
              Column(
                children: [
                  _supportState == _SupportState.supported ? Text("supported") : Text("notSupported"),
                  Text(authCheck.toString()),
                  // Center(
                  //   child: SvgPicture.asset(
                  //       "assets/images/fingerprint_recognition.svg",
                  //       width: 200,
                  //       height: 200,
                  //       semanticsLabel: "Label"
                  //   ),
                  // ),
                ],
              ),
              const SizedBox(
                height: 80,
              ),
            ],
          ),
        ),
      ),
    );
  }

  final LocalAuthentication auth = LocalAuthentication();
  _SupportState _supportState = _SupportState.unknown;
  bool? _canCheckBiometrics;
  List<BiometricType>? _availableBiometrics;
  String _authorized = "Authenticating";
  bool _isAuthenticating = false;




  Future<void> _authenticate() async {
    bool authenticated = false;
    try {
      setState(() {
        _isAuthenticating = true;
        _authorized = "Authenticating";
      });
      authenticated = await auth.authenticate(
        localizedReason: "Let OS determine authentication method",
        options: const AuthenticationOptions(
          stickyAuth: true,
        ),
      );
      setState(() {
        _isAuthenticating = false;
      });
    } on PlatformException catch (e) {
      print(e);
      setState(() {
        _isAuthenticating = false;
        _authorized = "Error - ${e.message}";
      });
      return;
    }
    if (!mounted) {
      return;
    }

    setState(
            () => _authorized = authenticated ? "Authorized" : "Not Authorized");
  }


  //device ID

  Future<void> initPlatformState() async {
    var deviceData = <String, dynamic>{};

    try {
      if (kIsWeb) {
        deviceData = _readWebBrowserInfo(await deviceInfoPlugin.webBrowserInfo);
      } else {
        if (Platform.isAndroid) {
          deviceData =
              _readAndroidBuildData(await deviceInfoPlugin.androidInfo);
        } else if (Platform.isIOS) {
          deviceData = _readIosDeviceInfo(await deviceInfoPlugin.iosInfo);
        } else if (Platform.isLinux) {
          deviceData = _readLinuxDeviceInfo(await deviceInfoPlugin.linuxInfo);
        } else if (Platform.isMacOS) {
          deviceData = _readMacOsDeviceInfo(await deviceInfoPlugin.macOsInfo);
        } else if (Platform.isWindows) {
          deviceData =
              _readWindowsDeviceInfo(await deviceInfoPlugin.windowsInfo);
        }
      }
    } on PlatformException {
      deviceData = <String, dynamic>{
        'Error:': 'Failed to get platform version.'
      };
    }

    if (!mounted) return;

    setState(() {
      _deviceData = deviceData;
    });
  }

  Map<String, dynamic> _readAndroidBuildData(AndroidDeviceInfo build) {
    return <String, dynamic>{
